  components: {
    Breadcrumb,
    Hamburger
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'name'
    ])
  },
  methods: {
    toggleSideBar () {
      this.$store.dispatch(appTypes.TOGGLE_SIDEBAR)
    },
    logout () {
      this.$store.dispatch(userTypes.LOGOUT).then(() => {
        window.location.reload()
      })
    }
  }
}
</script>

